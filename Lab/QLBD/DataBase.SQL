-- Tạo bảng QUOCGIA
CREATE TABLE QUOCGIA (
	MAQG VARCHAR(5),
	TENQG NVARCHAR(60) NOT NULL,
	PRIMARY KEY (MAQG)
);

-- Tạo bảng TINH
CREATE TABLE TINH (
	MATINH VARCHAR(5),
	TENTINH NVARCHAR(100) NOT NULL, 
	PRIMARY KEY (MATINH)
);

-- Tạo bảng TABLE
CREATE TABLE SANVD (
	MASAN VARCHAR(5),
	TENSAN NVARCHAR(100) NOT NULL, 
	DIACHI NVARCHAR(100),
	PRIMARY KEY (MASAN)
);

-- Tạo bảng CAULACBO
CREATE TABLE CAULACBO (
	MACLB VARCHAR(5),
	TENCLB NVARCHAR(100) NOT NULL, 
	MASAN VARCHAR(5) NOT NULL, 
	MATINH VARCHAR(5) NOT NULL, 
	PRIMARY KEY (MACLB),
	FOREIGN KEY (MATINH) REFERENCES TINH,
	FOREIGN KEY (MASAN) REFERENCES SANVD
);

-- Tạo bảng CAUTHU
CREATE TABLE CAUTHU (
	MACT NUMERIC IDENTITY(1, 1),
	HOTEN NVARCHAR(100) NOT NULL,
	VITRI NVARCHAR(20) NOT NULL, 
	NGAYSINH DATETIME, 
	DIACHI NVARCHAR(200), 
	MACLB VARCHAR(5) NOT NULL, 
	MAQG VARCHAR(5) NOT NULL, 
	SO INT NOT NULL, 
	PRIMARY KEY (MACT),
	FOREIGN KEY (MAQG) REFERENCES QUOCGIA,
	FOREIGN KEY (MACLB) REFERENCES CAULACBO
);

-- Tạo bảng HUANLUYENVIEN
CREATE TABLE HUANLUYENVIEN (
	MAHLV VARCHAR(5),
	TENHLV NVARCHAR(100) NOT NULL, 
	NGAYSINH DATETIME,
	DIACHI NVARCHAR(100),
	DIENTHOAI NVARCHAR(20),
	MAQG VARCHAR(5) NOT NULL,
	PRIMARY KEY (MAHLV),
	FOREIGN KEY (MAQG) REFERENCES QUOCGIA
);

-- Tạo bảng HLV_CLB
CREATE TABLE HLV_CLB (
	MAHLV VARCHAR(5),
	MACLB VARCHAR(5),
	VAITRO NVARCHAR(100) NOT NULL,
	PRIMARY KEY (MAHLV, MACLB),
	FOREIGN KEY (MAHLV) REFERENCES HUANLUYENVIEN,
	FOREIGN KEY (MACLB) REFERENCES CAULACBO
);

CREATE TABLE TRANDAU (
	MATRAN NUMERIC IDENTITY(1, 1),
	NAM INT NOT NULL, 
	VONG INT NOT NULL, 
	NGAYTD DATETIME NOT NULL, 
	MACLB1 VARCHAR(5) NOT NULL, 
	MACLB2 VARCHAR(5) NOT NULL, 
	MASAN VARCHAR(5) NOT NULL, 
	KETQUA VARCHAR(5) NOT NULL,
	PRIMARY KEY (MATRAN),
	FOREIGN KEY (MASAN) REFERENCES SANVD,
	FOREIGN KEY (MACLB1) REFERENCES CAULACBO,
	FOREIGN KEY (MACLB2) REFERENCES CAULACBO
);

-- Tạo bảng BANGXH
CREATE TABLE BANGXH (
	MACLB VARCHAR(5),
	NAM INT,
	VONG INT,
	SOTRAN INT NOT NULL, 
	THANG INT NOT NULL,
	HOA INT NOT NULL, 
	THUA INT NOT NULL, 
	HIEUSO VARCHAR(5) NOT NULL,
	DIEM INT NOT NULL,
	HANG INT NOT NULL,
	PRIMARY KEY (MACLB, NAM, VONG),
	FOREIGN KEY (MACLB) REFERENCES CAULACBO
);

-- Tạo bảng THAMGIA
CREATE TABLE THAMGIA (
	MATD NUMERIC,
	MACT NUMERIC,
	SOTRAI INT,
	PRIMARY KEY (MATD, MACT),
	FOREIGN KEY (MATD) REFERENCES TRANDAU(MATRAN),
	FOREIGN KEY (MACT) REFERENCES CAUTHU
);

