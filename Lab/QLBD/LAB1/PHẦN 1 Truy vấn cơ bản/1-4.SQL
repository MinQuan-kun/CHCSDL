--1. Cho biết mã số, họ tên, ngày sinh, địa chỉ và vị trí của các cầu thủ thuộc đội bóng “SHB Đà Nẵng” có quốc tịch “Bra-xin”.
SELECT	CAUTHU.MACT,
		HOTEN,
		NGAYSINH,
		DIACHI,
		VITRI
FROM CAUTHU
	JOIN QUOCGIA ON QUOCGIA.MAQG = CAUTHU.MAQG
	JOIN CAULACBO ON CAULACBO.MACLB = CAUTHU.MACLB
WHERE
	QUOCGIA.TENQG = N'Bra-xin'
	AND CAULACBO.TENCLB = N'SHB Đà Nẵng';

--2. Cho biết tên cầu thủ đã ghi từ 2 bàn thắng trở lên trong một trận đấu.
SELECT
	CAUTHU.HOTEN
FROM CAUTHU
	JOIN THAMGIA ON CAUTHU.MACT = THAMGIA.MACT
WHERE
	THAMGIA.SOTRAI >= 2

--3. Cho biết kết quả (MATRAN, NGAYTD, TENSAN, TENCLB1, TENCLB2, KETQUA) các trận đấu thuộc vòng 3 của mùa bóng năm 2009.
SELECT TRANDAU.MATRAN,
		TRANDAU.NGAYTD,
		SANVD.TENSAN,
		CLB1.TENCLB AS TENCLB1,
		CLB2.TENCLB AS TENCLB2,
		TRANDAU.KETQUA
FROM TRANDAU
	JOIN CAULACBO CLB1 ON CLB1.MACLB = TRANDAU.MACLB1
	JOIN CAULACBO CLB2 ON CLB2.MACLB = TRANDAU.MACLB2
	JOIN SANVD ON SANVD.MASAN = TRANDAU.MASAN
WHERE
	TRANDAU.VONG = '3'
	AND TRANDAU.NAM = '2009';
    
--4. Cho biết mã huấn luyện viên, họ tên, ngày sinh, địa chỉ, vai trò và tên CLB đang làm việc của các huấn luyện viên có quốc tịch “Việt Nam”.
SELECT	HUANLUYENVIEN.MAHLV,
		TENHLV,
		NGAYSINH,
		DIACHI,
		HLV_CLB.VAITRO,
		CLB.TENCLB
FROM HUANLUYENVIEN
		JOIN HLV_CLB ON HLV_CLB.MAHLV = HUANLUYENVIEN.MAHLV
		JOIN QUOCGIA ON HUANLUYENVIEN.MAQG = QUOCGIA.MAQG
		JOIN CAULACBO CLB ON HLV_CLB.MACLB = CLB.MACLB
WHERE
	QUOCGIA.TENQG = N'Việt Nam'