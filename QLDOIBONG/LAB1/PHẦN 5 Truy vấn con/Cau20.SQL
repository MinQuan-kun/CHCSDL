--Cho biết danh sách các trận đấu (NGAYTD, TENSAN, TENCLB1, TENCLB2, KETQUA) của câu lạc bộ CLB có thứ hạng thấp nhất trong bảng xếp hạng vòng 3 năm 2009.
WITH "DOIYEU" AS (SELECT TOP 1 BXH.HANG,
	BXH.MACLB
FROM BANGXH BXH
WHERE BXH.NAM = 2009
AND BXH.VONG = 3
ORDER BY HANG DESC
) 

SELECT TD.NGAYTD ,
	SAN.TENSAN,
	CLB1.TENCLB,
	CLB2.TENCLB,
	TD.KETQUA
FROM TRANDAU TD
JOIN CAULACBO CLB1 ON CLB1.MACLB = TD.MACLB1
JOIN CAULACBO CLB2 ON CLB2.MACLB = TD.MACLB2
JOIN SANVD SAN ON SAN.MASAN = TD.MASAN
WHERE TD.MACLB1 =(SELECT MACLB FROM DOIYEU)
OR TD.MACLB2 = (SELECT MACLB FROM DOIYEU)
