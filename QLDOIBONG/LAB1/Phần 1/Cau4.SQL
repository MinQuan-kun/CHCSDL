SELECT 
    HLV.MAHLV, 
    HLV.TENHLV, 
    HLV.NGAYSINH, 
    HLV.DIACHI, 
    HLV_CLB.VAITRO, 
    CLB.TENCLB
FROM 
    HUANLUYENVIEN HLV
    JOIN QUOCGIA QG ON HLV.MAQG = QG.MAQG
    JOIN HLV_CLB ON HLV.MAHLV = HLV_CLB.MAHLV
    JOIN CAULACBO CLB ON HLV_CLB.MACLB = CLB.MACLB
WHERE 
    QG.TENQG = 'Viet Nam';
