--Cho biết kết quả (MATRAN, NGAYTD, TENSAN, TENCLB1, TENCLB2, KETQUA) các trận đấu thuộc vòng 3 của mùa bóng năm 2009.
SELECT 
    TD.MATRAN, 
    TD.NGAYTD, 
    SVD.TENSAN, 
    CLB1.TENCLB AS TENCLB1, 
    CLB2.TENCLB AS TENCLB2, 
    TD.KETQUA
FROM 
    TRANDAU TD
    JOIN SANVD SVD ON TD.MASAN = SVD.MASAN
    JOIN CAULACBO CLB1 ON TD.MACLB1 = CLB1.MACLB
    JOIN CAULACBO CLB2 ON TD.MACLB2 = CLB2.MACLB
WHERE 
    TD.NAM = 2009 
    AND TD.VONG = 3;
