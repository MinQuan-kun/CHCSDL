--Cho biết kết quả (MATRAN, NGAYTD, TENSAN, TENCLB1, TENCLB2, KETQUA) các trận đấu thuộc vòng 3 của mùa bóng năm 2009.
SELECT TRANDAU.MATRAN,
		TRANDAU.NGAYTD,
		SANVD.TENSAN,
		CLB1.TENCLB AS TENCLB1,
		CLB2.TENCLB AS TENCLB2,
		TRANDAU.KETQUA
FROM TRANDAU
	JOIN CAULACBO CLB1 ON CLB1.MACLB = TRANDAU.MACLB1
	JOIN CAULACBO CLB2 ON CLB2.MACLB = TRANDAU.MACLB2
	JOIN SANVD ON SANVD.MASAN = TRANDAU.MASAN
WHERE
	TRANDAU.VONG = '3'
	AND TRANDAU.NAM = '2009';